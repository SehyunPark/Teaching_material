import turtle

siri = turtle.Turtle()

siri.color('#4263f5', '#ad42f5')

siri.begin_fill()

siri.forward(100)
siri.left(90)
siri.forward(100)
siri.left(90)
siri.forward(100)
siri.left(90)
siri.forward(100)

siri.penup()
siri.forward(150)
siri.pendown()

siri.forward(100)
siri.left(90)
siri.forward(100)
siri.left(90)
siri.forward(100)
siri.left(90)
siri.forward(100)

siri.end_fill()

turtle.done()